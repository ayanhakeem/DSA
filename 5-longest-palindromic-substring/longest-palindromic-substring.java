class Solution {
    public static boolean ispalindrome(String s){
        int n=s.length();
        int l=0,r=n-1;
        while(l<r){
            if(s.charAt(l)!=s.charAt(r)){
                return false;
            }
            l++;
            r--;
        }
        return true;
    }
    public String longestPalindrome(String s) {
        int n=s.length();
        String ans="";
        for(int i=0;i<n;i++){
            for(int j=i;j<n;j++){
                String sub=s.substring(i,j+1);    
                if(ispalindrome(sub) && sub.length()>ans.length()){
                    ans=sub;
                }
            }
        }
        return ans;
        
    }
}